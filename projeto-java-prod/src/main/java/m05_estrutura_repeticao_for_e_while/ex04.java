package m05_estrutura_repeticao_for_e_while;

public class ex04 {

	public static void main(String[] args) {

		/* While = Repita enquanto seja verdadeiro a expressão */

		int contador = 0;

		while (contador <= 10) {/*Enquanto o contador for menor ou igual a 10*/
			System.out.println(contador);
			contador++;
		}

	}

}
