package variaveistipoeoperador;

public class UnboxiEautoBoxing {
	
	public static void main(String[] args) {
	
		//Autoboxing É a conversão automática de um tipo primitivo para sua classe Wrapper.
		int idade = 38;
		
		//Autoboxing
		Integer idadeWeapper = idade;
		
		
		
		
		
		//Unboxing É a conversão automática de uma classe Wrapper para um tipo primitivo.
		Integer idadeWrapper3 = 39;
		
		//Unboxing
		int idadeprimitiva = idadeWrapper3;
		
		
	}

}
