package m01_classe_mail;

public class ClasseTipoDado {
	
	
	public static void main(String[] args) {
		
		// representação de idade em numero inteiro.
		int idade = 38;
		
		/*Representação da idade em fração*/
		double idadeMesesMaternidade = 2.6;
		
		/*Repreenta valores de sim = true e não = false*/
		boolean usuarioAtivo = true;
		
		/*Representa texto*/
		String nome = "Alex fernando Egidio";
		
		/*Representa um letra que representa algum tipo de algo no mundo real*/
		char sexoPessoa = 'M';  
		
		
		System.out.println("Idade: " + idade);
		System.out.println("Idade marterniade : " + idadeMesesMaternidade);
		System.out.println("Usuário ativo: " + usuarioAtivo);
		System.out.println("Nome da pessoa: " + nome);
		System.out.println("Sexo da pessoa: " + sexoPessoa);
	}

}
